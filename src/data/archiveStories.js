// Helper function to group archives by month/year
export const getArchivesByMonth = () => {
    const grouped = {};
    archiveStories.forEach(story => {
        const date = new Date(story.date);
        const key = `${date.toLocaleString('en-US', { month: 'long' })} ${date.getFullYear()}`;
        if (!grouped[key]) {
            grouped[key] = [];
        }
        grouped[key].push(story);
    });
    return grouped;
};

// Helper function to get archive months list
export const getArchiveMonths = () => {
    const months = {};
    archiveStories.forEach(story => {
        const date = new Date(story.date);
        const key = `${date.toLocaleString('en-US', { month: 'long' })} ${date.getFullYear()}`;
        if (!months[key]) {
            months[key] = { label: key, count: 0, year: date.getFullYear(), month: date.getMonth() };
        }
        months[key].count++;
    });
    // Sort by date descending
    return Object.values(months).sort((a, b) => {
        if (b.year !== a.year) return b.year - a.year;
        return b.month - a.month;
    });
};
